        if layer == 0:
            return output

        h = self.slice2(h)
        h_relu2_2 = h
        output.append(h_relu2_2)
        if layer == 1:
            return output

        h = self.slice3(h)
        h_relu3_3 = h
        output.append(h_relu3_3)
        if layer == 2:
            return output
        
        h = self.slice4(h)
        h_relu4_3 = h
        output.append(h_relu4_3)
        if layer == 3:
            return output



